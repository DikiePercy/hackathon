const SUPPORTED_LANGS = ["ru", "ky", "en", "tr"];

function normalizeLang(lang) {
    return SUPPORTED_LANGS.includes(lang) ? lang : "ru";
}

function getCurrentLanguage() {
    return normalizeLang(localStorage.getItem("archive_lang") || "ru");
}

function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang]?.[key] || translations.ru[key] || key;
}

function setLanguage(lang) {
    const normalized = normalizeLang(lang);
    localStorage.setItem("archive_lang", normalized);
    applyLanguage(normalized);
}

function applyElementText(element, value, mode) {
    if (!element || !value) return;
    if (mode === "prefix-strong") {
        const strong = element.querySelector("strong");
        if (strong) {
            element.textContent = `${value} `;
            element.appendChild(strong);
            return;
        }
    }
    element.textContent = value;
}

function applyBinding(binding, dict) {
    const element = document.querySelector(binding.selector);
    if (!element) return;
    const value = dict[binding.key] || translations.ru[binding.key];
    if (!value) return;

    if (binding.attr) {
        element.setAttribute(binding.attr, value);
        return;
    }

    applyElementText(element, value, binding.mode);
}

function applyPageBindings(lang) {
    const dict = translations[lang] || translations.ru;
    const page = (window.location.pathname.split("/").pop() || "index.html").toLowerCase();
    const bindings = [...PAGE_BINDINGS.shared, ...(PAGE_BINDINGS[page] || [])];

    bindings.forEach((binding) => applyBinding(binding, dict));

    const titleKey = `title_${page.replace(".html", "")}`;
    if (dict[titleKey]) {
        document.title = dict[titleKey];
    }
}

function applyLanguage(lang) {
    const normalized = normalizeLang(lang);
    const dict = translations[normalized] || translations.ru;

    document.documentElement.lang = normalized;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (!dict[key]) return;

        if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
            el.placeholder = dict[key];
        } else {
            el.textContent = dict[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (dict[key]) el.setAttribute("placeholder", dict[key]);
    });

    applyPageBindings(normalized);

    document.querySelectorAll(".lang-btn").forEach((btn) => {
        const btnLang = btn.getAttribute("data-lang");
        btn.classList.toggle("active", btnLang === normalized);
    });

    window.dispatchEvent(new CustomEvent("site-language-changed", { detail: { lang: normalized } }));
}

function ensureLanguageSwitcher() {
    if (document.querySelector(".lang-switcher")) return;

    const host = document.body;
    const switcher = document.createElement("div");
    switcher.className = "lang-switcher";

    SUPPORTED_LANGS.forEach((lang) => {
        const btn = document.createElement("button");
        btn.type = "button";
        btn.className = "lang-btn";
        btn.setAttribute("data-lang", lang);
        btn.textContent = lang.toUpperCase();
        btn.setAttribute("aria-label", `Switch language to ${lang.toUpperCase()}`);
        btn.addEventListener("click", () => setLanguage(lang));
        switcher.appendChild(btn);
    });

    host.appendChild(switcher);
}

window.AppI18n = {
    setLanguage,
    applyLanguage,
    getCurrentLanguage,
    t
};

window.setLanguage = setLanguage;

document.addEventListener("DOMContentLoaded", () => {
    ensureLanguageSwitcher();

    const savedLang = getCurrentLanguage();
    applyLanguage(savedLang);

    // Run once again after dynamic header render.
    setTimeout(() => applyLanguage(savedLang), 0);

    window.addEventListener("site-auth-changed", () => {
        applyLanguage(getCurrentLanguage());
    });

    // Ensure language stays consistent after page transitions/back-forward cache restore.
    window.addEventListener("pageshow", () => {
        applyLanguage(getCurrentLanguage());
    });

    // Sync language if another tab/page updates localStorage.
    window.addEventListener("storage", (event) => {
        if (event.key === "archive_lang") {
            applyLanguage(getCurrentLanguage());
        }
    });
});